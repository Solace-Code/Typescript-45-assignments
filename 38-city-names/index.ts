// Objective Write a function that takes input of a country and its city and prints it out //

// Creating the function //

function city_country(city:string, country:string){
    console.log("\n" + `${city}` + " is in " + `${country}`);
}

// Creating a variable to store the value inside the function //

city_country("Karachi", "Pakistan");
city_country("Kyoto", "Japan");
city_country("Paris", "France");
city_country("Shanghai", "China");